import os

def remove_files(directory, file_list):
    """
    Given a path to a directory and a list of files,
    remove them from directory.
    """
    count = 0
    not_exist_count = 0
    for file in file_list:
        if os.path.exists(os.path.join(directory,get_species_folder(file), file)):
            try:
                os.remove(os.path.join(directory,get_species_folder(file), file))   
                count +=1
            except:
                print(f"Unable to remove image {file}")
        else:
            not_exist_count +=1
        
    print(f"{not_exist_count} files not found. {count} files removed.")

def get_species_folder(file_name):
    """
    Return a string with the name of the species folder.
    Arg.: a file name with the format:
          Genus_species_occurrencenumber.jpg
    """
    file_split = file_name.split("_")
    # Remove occurence number and file extension
    folder_name_splitted = file_split[:-1]
    return "_".join(folder_name_splitted)


